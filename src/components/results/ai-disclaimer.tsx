import { Card, CardContent } from "@/components/ui/card";
import { AlertTriangle } from "lucide-react";
import type { AnalysisMetadata } from "@/types";
import { PROVIDER_LABELS } from "@/lib/constants";

interface AiDisclaimerProps {
  metadata: AnalysisMetadata;
}

export function AiDisclaimer({ metadata }: AiDisclaimerProps) {
  const date = new Date(metadata.timestamp);
  const formatted = date.toLocaleDateString("en-US", {
    year: "numeric",
    month: "long",
    day: "numeric",
    hour: "2-digit",
    minute: "2-digit",
    timeZoneName: "short",
  });

  return (
    <Card className="border-yellow-200 dark:border-yellow-800 bg-yellow-50/50 dark:bg-yellow-950/20">
      <CardContent className="pt-6">
        <div className="flex gap-3">
          <AlertTriangle className="h-5 w-5 text-yellow-500 shrink-0 mt-0.5" />
          <div className="space-y-2 text-sm">
            <p>
              This report was generated by AI. AI can make mistakes. Use this as
              guidance, not as a definitive assessment. Always apply your own judgment.
            </p>
            <div className="text-xs text-muted-foreground space-y-0.5">
              <p>Provider: {PROVIDER_LABELS[metadata.provider] || metadata.provider} ({metadata.model})</p>
              <p>Generated: {formatted}</p>
            </div>
          </div>
        </div>
      </CardContent>
    </Card>
  );
}
